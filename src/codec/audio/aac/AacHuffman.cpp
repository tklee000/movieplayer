bool AacHuffman::DecodeScaleFactor(BitReader& bits, int& difference) {
    int symbol = 0;
    if (!DecodeSymbol(bits, 12, symbol)) return false;
    difference = symbol - 60;
    return true;
}

bool AacHuffman::DecodeSpectral(BitReader& bits, unsigned codebook,
                                std::array<int, 4>& values,
                                unsigned& dimension) {
    if (codebook < 1 || codebook > 11) return false;
    int symbol = 0;
    if (!DecodeSymbol(bits, codebook, symbol)) return false;
    const bool quadruple = codebook <= 4;
    dimension = quadruple ? 4U : 2U;
    unsigned modulus = 0;
    int offset = 0;
    bool signedInCodebook = false;
    if (codebook <= 4) {
        modulus = 3; offset = codebook <= 2 ? 1 : 0;
        signedInCodebook = codebook <= 2;
    } else if (codebook <= 6) {
        modulus = 9; offset = 4; signedInCodebook = true;
    } else if (codebook <= 8) {
        modulus = 8;
    } else if (codebook <= 10) {
        modulus = 13;
    } else {
        modulus = 17;
    }
    values.fill(0);
    for (int i = static_cast<int>(dimension) - 1; i >= 0; --i) {
        values[static_cast<std::size_t>(i)] =
            symbol % static_cast<int>(modulus) - offset;
        symbol /= static_cast<int>(modulus);
    }
    if (!signedInCodebook) {
        for (unsigned i = 0; i < dimension; ++i) {
            if (!ReadSignedMagnitude(bits, values[i])) return false;
        }
    }
    if (codebook == 11) {
        for (unsigned i = 0; i < dimension; ++i) {
            if (!ReadEscape(bits, values[i])) return false;
        }
    }
    return true;
}

