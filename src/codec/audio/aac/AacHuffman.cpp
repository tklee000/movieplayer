struct Node {
    int child[2] = {-1, -1};
    int symbol = -1;
};

struct Trie {
    std::vector<Node> nodes{1};
    unsigned maximumLength = 0;
};

template <std::size_t N>
Trie BuildTrie(const HuffmanCode (&codes)[N]) {
    Trie trie;
    for (std::size_t symbol = 0; symbol < N; ++symbol) {
        int node = 0;
        trie.maximumLength = std::max<unsigned>(trie.maximumLength,
                                                codes[symbol].length);
        for (unsigned bit = codes[symbol].length; bit > 0; --bit) {
            const unsigned branch = (codes[symbol].code >> (bit - 1U)) & 1U;
            if (trie.nodes[node].child[branch] < 0) {
                trie.nodes[node].child[branch] =
                    static_cast<int>(trie.nodes.size());
                trie.nodes.push_back({});
            }
            node = trie.nodes[node].child[branch];
        }
        trie.nodes[node].symbol = static_cast<int>(symbol);
    }
    return trie;
}

const Trie& Book(unsigned codebook) {
    static const std::array<Trie, 12> books = {
        BuildTrie(kBook1), BuildTrie(kBook2), BuildTrie(kBook3),
        BuildTrie(kBook4), BuildTrie(kBook5), BuildTrie(kBook6),
        BuildTrie(kBook7), BuildTrie(kBook8), BuildTrie(kBook9),
        BuildTrie(kBook10), BuildTrie(kBook11), BuildTrie(kBook12)};
    return books.at(codebook - 1U);
}

bool DecodeSymbol(BitReader& bits, unsigned codebook, int& symbol) {
    const Trie& trie = Book(codebook);
    int node = 0;
    for (unsigned length = 0; length < trie.maximumLength; ++length) {
        bool bit = false;
        if (!bits.ReadBit(bit)) return false;
        node = trie.nodes[node].child[bit ? 1 : 0];
        if (node < 0) return false;
        if (trie.nodes[node].symbol >= 0) {
            symbol = trie.nodes[node].symbol;
            return true;
        }
    }
    return false;
}

bool ReadSignedMagnitude(BitReader& bits, int& value) {
    if (value == 0) return true;
    bool negative = false;
    if (!bits.ReadBit(negative)) return false;
    if (negative) value = -value;
    return true;
}

bool ReadEscape(BitReader& bits, int& value) {
    if (value != 16 && value != -16) return true;
    const int sign = value < 0 ? -1 : 1;
    unsigned exponent = 4;
    bool more = false;
    do {
        if (!bits.ReadBit(more)) return false;
        if (more && ++exponent > 30) return false;
    } while (more);
    std::uint32_t remainder = 0;
    if (!bits.ReadBits(exponent, remainder)) return false;
    value = sign * static_cast<int>((std::uint32_t{1} << exponent) + remainder);
    return true;
}

